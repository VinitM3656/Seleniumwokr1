package JavaTpoint.Switch;

public class Switch {
	public static void main(String [] args) {
		
		int number = 568;
		
		switch(number) {
		case 10: System.out.println("10");
		
		 break;
		 
		case 20:
			
			System.out.println(" 20");
			break;

			
		case 30:
			System.out.println(" 30");
			break;
			
		case 40:
			System.out.println(" 40");
			
		case 50:
			System.out.println(" default choice");
			
		}
	}
}
	
		


