package Corejava.basics;

public class VariableDeclarationExample {

	public static void main(String[] args) {
		byte num1;
		int num2;
		char ch;
		boolean isCompleted;
		num1 = 10;
		num2 = 8786786;
		ch ='R';
		isCompleted= false;
		
		
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(ch);
		System.out.println(isCompleted);

	}

}
