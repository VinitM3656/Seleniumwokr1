package JavaPrograms;

import java.util.Arrays; 


public class ArrayToString {
    public static void main(String[] args) {
        int[] a1 = {1, 2, 3, 4, 5};
        int[] a2 = new int[5]; 

        System.out.println(Arrays.toString(a2)); 
        System.out.println(Arrays.toString(a1)); 
        System.out.println(Arrays.toString(a2)); 
    }
}
