package JavaPrograms;

public class StringWidth {
	public static void main(String args[]) {
		String s1 = "java by TechnoLamor";
		String s2 = "Hello how are you";
		
		System.out.println(s1.endsWith("r"));
		System.out.println(s1.endsWith("Lamor"));
		System.out.println(s1.endsWith("lamor"));
		
		System.out.println(s2.startsWith("H"));
		System.out.println(s2.startsWith("y"));
		System.out.println(s2.startsWith("Hello"));
	}

}
